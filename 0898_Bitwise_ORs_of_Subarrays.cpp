class Solution {
public:
    int subarrayBitwiseORs(vector<int>& arr) {
        unordered_set<int> st, answer;
        for(int i : arr){
            unordered_set<int> s;
            for(int j: st){
                s.insert(i|j);
                answer.insert(i|j);
            }
            s.insert(i);
            answer.insert(i);
            st = s;
        }
        return answer.size();
    }
};
