class Solution {
public:
    int maxUniqueSplit(string s) {
        unordered_set<string> st;
        int ans = 1;
        function<void(int, int)> ddfs = [&](int j, int t){
            if(j >= s.size()){
                ans = max(t, ans);
                return;
            }
            for(int i = j+1; i<=s.size(); i++){
                string x = s.substr(j, i-j);
                if(!st.count(x)){
                    st.insert(x);
                    ddfs(i, t+1);
                    st.erase(x);
                }
            }
        };
        ddfs(0, 0);
        return ans;
    }
};
