class Solution {
public:
    bool canBeEqual(vector<int>& target, vector<int>& arr) {

// Approch 1

        int at[1001] ={0};
        for(int i: arr){
            at[i]++;
        }
        for(int i: target){
            at[i]--;
        }
        for(int i=0; i<1001; i++){
            if(at[i]){
                return false;
            }
        }
        return true;

//Approch 2

        unordered_map<int, int> mp;
        for(int i : arr){
            mp[i]++;
        }
        for(int i: target){
            if(!mp[i]){
                return false;
            }
            mp[i]--;
        }
        return true;
    }
};
