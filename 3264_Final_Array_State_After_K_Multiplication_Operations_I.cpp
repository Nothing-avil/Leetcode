class Solution {
public:
    vector<int> getFinalState(vector<int>& nums, int k, int multiplier) {
        priority_queue<pair<int,int>, vector<pair<int, int>>, greater<pair<int,int>>> pq;
        for(int i=0; i<nums.size(); i++){
            pq.push({nums[i], i});
        }
        while(k--){
            int m = pq.top().first;
            int i = pq.top().second;
            pq.pop();
            m*=multiplier;
            pq.push({m,i});
        }
        while(pq.size()){
            int m = pq.top().first;
            int i = pq.top().second;
            pq.pop();
            nums[i] = m;
        }
        return nums;
    }
};
