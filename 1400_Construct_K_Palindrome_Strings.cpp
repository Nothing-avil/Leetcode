class Solution {
public:
    bool canConstruct(string s, int k) {
        if(s.size() < k){
            return false;
        }
        int mp[26] = {0};
        for(char ch : s){
            mp[ch - 'a']++;
        }
        int c =0;
        for(auto it : mp){
            c += it&1;
        }
        return c<= k;
    }
};
