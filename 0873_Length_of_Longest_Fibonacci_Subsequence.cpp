class Solution {
public:
    int lenLongestFibSubseq(vector<int>& arr) {
        int n = arr.size();
        unordered_map<int,int> mp;
        for(int i=0; i<n; i++){
            mp[arr[i]] = i;
        }
        int ans = 0;
        vector<vector<int>> dp(n, vector<int>(n, 2));
        for(int i = 0; i<n; i++){
            for(int j=i+1; j<n; j++){
                int di = arr[j] - arr[i];
                if(di < arr[i] && mp.count(di)){
                    int k = mp[di];
                    dp[i][j] = dp[k][i] + 1;
                    ans = max(ans , dp[i][j]);
                }
            }
        }
        return ans;
    }
};
