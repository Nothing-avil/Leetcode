class Solution {
public:
    int bitwiseComplement(int num) {
        if(num==0){
            return 1;
        }
        int ni=num;
        int c=0;
        while(ni>0){
            ni= ni>>1;
            c++;
        }
        ni = (1 << c) - 1;
        return (num ^ ni);
    }

};
