class Solution {
public:
    int minimumArea(vector<vector<int>>& grid) {
        int ai=INT_MAX, bi = INT_MAX, a = 0, b = 0;
        bool f = true;
        int n = grid.size(), m = grid[0].size();
        for(int i=0; i<n; i++){
            for(int j = 0; j<m; j++){
                if(grid[i][j] == 1){
                    ai = min(ai, i);
                    bi = min(bi, j);
                    a = max(a, i);
                    b = max(b, j);
                }
            }
        }
        a = a - ai + 1;
        b = b - bi + 1;
        return (a * b);
    }
};
