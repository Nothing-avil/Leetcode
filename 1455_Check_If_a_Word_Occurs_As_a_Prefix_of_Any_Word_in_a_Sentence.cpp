class Solution {
public:
    int isPrefixOfWord(string sentence, string searchWord) {
        stringstream ss(sentence);
        string t;
        int res= 0, j = searchWord.size();
        while(ss >> t){
            res++;
            int i=0;
            while(t[i] == searchWord[i] && i< j){
               i++; 
            }
            if(i==j){
                return res;
            }
        }
        return -1;
    }
};
