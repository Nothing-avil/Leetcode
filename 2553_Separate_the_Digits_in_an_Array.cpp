class Solution {
public:
    vector<int> separateDigits(vector<int>& nums) {
        vector<int> answer;
        for(int i: nums){
            vector<int> t;
            for (; i; i /= 10) {
                t.push_back(i % 10);
            }
            while (t.size()) {
                answer.push_back(t.back());
                t.pop_back();
            }
        }
        return answer;
    }
};
