class Solution {
public:
    int countSubarrays(vector<int>& nums) {
        int i = 2;
        int count = 0;
        int n = nums.size();
        while( i < n){
            if( (nums[i] + nums[i-2]) * 2 == (nums[i-1])){
                count++;
            }
            i++;
        }
        return count;
    }
};
