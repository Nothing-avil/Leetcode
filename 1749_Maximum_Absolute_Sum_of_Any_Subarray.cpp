class Solution {
public:
    int maxAbsoluteSum(vector<int>& nums) {
        int maxs = INT_MIN, mins = INT_MAX;
        int s = 0, ss = 0;
        for(int i : nums){
            s+=i;
            ss +=i;
            if(i > s){
                s = i;
            }
            if( i < ss){
                ss = i;
            }
            maxs = max(maxs, s);
            mins = min(mins, ss);
        }
        mins = abs(mins);
        return max(maxs, mins);
    }
};
