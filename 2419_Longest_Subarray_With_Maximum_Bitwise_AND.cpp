class Solution {
public:
    int longestSubarray(vector<int>& nums) {
        int res = 0, ans = 0;
        int maxi = *max_element(nums.begin(), nums.end());
        for(int i=1; i<nums.size(); i++){
            if(nums[i] == maxi && nums[i - 1] == maxi){
                ans++;
            }
            else{
                ans = 0;
            }
            res = max(res, ans);
        }
        return res + 1;
    }
};
