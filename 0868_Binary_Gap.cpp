class Solution {
public:
    int binaryGap(int n) {
        int ans = 0;
        for(int prev = 100, curr = 0; n!=0; n>>=1){
            if(n&1){
                ans = max(ans, curr - prev);
                prev = curr;
            }
            curr++;
        }
        return ans;
    }
};
