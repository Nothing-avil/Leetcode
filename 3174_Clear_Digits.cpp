class Solution {
public:
    string clearDigits(string s) {
        stack<char> st;
        for(char ch : s){
            if(isdigit(ch)){
                if(!st.empty()){
                    st.pop();
                }
                continue;
            }
            st.push(ch);
        }
        if(st.size() == 0){
            return "";
        }
        string ans = "";
        while(st.size()){
            ans = st.top() + ans;
            st.pop();
        }
        return ans;
    }
};
