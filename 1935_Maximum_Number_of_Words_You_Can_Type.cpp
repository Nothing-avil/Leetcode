class Solution {
public:
    int canBeTypedWords(string text, string brokenLetters) {
        stringstream ss(text);
        string t;
        int count = 0;
        unordered_map<char, int> mp;
        for(char ch : brokenLetters){
            mp[ch]++;
        }
        function<bool(string)> check = [&](string st){
            for(char ch : st){
                if(mp[ch]){
                    return false;
                }
            }
            return true;
        };
        while(ss >> t){
            if(check(t)){
                count++;
            }
        }
        return count;
    }
};
