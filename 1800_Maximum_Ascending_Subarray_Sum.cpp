class Solution {
public:
    int maxAscendingSum(vector<int>& nums) {
        int sum = nums[0];
        int ans = 0;
        for(int i=1; i<nums.size(); i++){
            if(nums[i-1] < nums[i]){
                sum += nums[i];
            }
            else{
                ans = max(ans, sum);
                sum = nums[i];
            }
        }
        ans = max(ans, sum);
        return ans;
    }
};
