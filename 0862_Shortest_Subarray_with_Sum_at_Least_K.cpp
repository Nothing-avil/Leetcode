class Solution {
public:
    int shortestSubarray(vector<int>& nums, int k) {
        int n = nums.size();
        vector<long> pre(n+1);
        for(int i=0; i<n; i++){
            pre[i+1] = pre[i] + nums[i];
        }
        deque<int> dq;
        int res = n+1;
        for(int i=0; i<=n; i++){
            while(!dq.empty() && pre[i] - pre[dq.front()] >= k){
                res = min(res, i-dq.front());
                dq.pop_front();
            }
            while(!dq.empty() && pre[dq.back()] >= pre[i]){
                dq.pop_back();
            }
            dq.push_back(i);
        }
        return res > n ? -1 : res;
    }
};
