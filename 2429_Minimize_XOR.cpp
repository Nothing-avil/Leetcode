class Solution {
public:
    int minimizeXor(int num1, int num2) {
        while(true){
            int n = __builtin_popcount(num1);
            int m = __builtin_popcount(num2);
            if(n == m){
                return num1;
            }
            else if(n > m){
                num1 = num1&(num1 - 1);
            }
            else{
                num1 = num1|(num1 + 1);
            }
        }
        return -1;
    }
};
