class Solution {
public:
    bool canArrange(vector<int>& arr, int k) {
        vector<int> freq(k);
        for(int i : arr){
            freq[((i%k)+k)%k]++;
        }
        for(int i=1; i<k; i++){
            if(freq[i] != freq[k-i]){
                return false;
            }
        }
        return freq[0]%2==0;
    }
};
