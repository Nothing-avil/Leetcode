class Solution {
public:
    int maximumBeauty(vector<int>& nums, int k) {
        int maxi = *max_element(nums.begin(), nums.end()) + k*2+2;
        vector<int> dp(maxi);
        for(int i : nums){
            dp[i]++;
            dp[i+2*k+1]--;
        }
        int ans = 0;
        int s = 0;
        for(int i : dp){
            s+=i;
            ans = max(ans, s);
        }
        return ans;
    }
};
