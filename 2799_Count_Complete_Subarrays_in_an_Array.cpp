class Solution {
public:
    int countCompleteSubarrays(vector<int>& nums) {
        unordered_map<int, int> mp;
        for(int i : nums){
            mp[i] = 1;
        }
        int n = mp.size();
        mp.clear();
        int answer =0;
        for(int i=0, j = 0; j < nums.size(); j++){
            mp[nums[j]]++;
            while(mp.size() == n){
                answer += (nums.size() - j);
                if(--mp[nums[i]] == 0){
                    mp.erase(nums[i]);
                }
                i++;
            }
        }
        return answer;
    }
};
