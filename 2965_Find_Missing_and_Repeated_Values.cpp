class Solution {
public:
    vector<int> findMissingAndRepeatedValues(vector<vector<int>>& grid) {
        int n = grid.size();
        vector<int> temp(n*n + 1);
        int dup = -1;
        for(int i = 0; i < n; i++){
            for(int j = 0; j < n; j++){
                temp[grid[i][j]]++;
                if(temp[grid[i][j]] == 2){
                    dup = grid[i][j];
                }
            }
        }
        for(int i = 1; i <= n*n; i++){
            if(temp[i] == 0){
                return {dup, i};
            }
        }
        return {-1, -1};
    }
};
