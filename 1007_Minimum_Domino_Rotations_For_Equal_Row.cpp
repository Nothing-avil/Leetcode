class Solution {
public:
    int minDominoRotations(vector<int>& tops, vector<int>& bottoms) {
        int answer = INT_MAX;
        for(int i=1; i<=6; i++){
            int val = 0;
            int top = 0, bottom = 0;
            for(int j=0; j<tops.size(); j++){
                if(tops[j] != i && bottoms[j] != i){
                    val = -1;
                    break;
                }else if(tops[j] != i){
                    top++;
                }else if(bottoms[j] != i){
                    bottom++;
                }
                val = min(top, bottom);
            }
            if(val != -1){
                answer = min(answer, val);
            }
        }
        return answer == INT_MAX ? -1 : answer;
    }
};
