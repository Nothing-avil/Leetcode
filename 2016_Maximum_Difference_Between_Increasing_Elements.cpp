class Solution {
public:
    int maximumDifference(vector<int>& nums) {
        int answer = INT_MIN;
        for(int i=0; i<nums.size() - 1; i++){
            for(int j=i+1; j<nums.size(); j++){
                if(nums[i] < nums[j]){
                    answer = max(answer, (nums[j] - nums[i]));
                }
            }
        }
        return answer == INT_MIN ? -1 : answer;
    }
};
