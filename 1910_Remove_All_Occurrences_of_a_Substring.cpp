class Solution {
public:
    string removeOccurrences(string s, string part) {
        string str = "";
        int n = part.size();
        for(char ch : s){
            str+= ch;
            if(str.size() >= n && part == str.substr(str.size() - n)){
                str.erase(str.size() - n);
            }
        }
        return str;
    }
};
