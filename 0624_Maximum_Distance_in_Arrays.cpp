class Solution {
public:
    int maxDistance(vector<vector<int>>& arrays) {
        int start = arrays[0].front();
        int end = arrays[0].back();
        int res=0;
        for(int i=1; i<arrays.size(); i++){
            int starts=arrays[i].front();
            int ends = arrays[i].back();
            res= max({res, abs(ends-start), abs(end-starts)});
            start= min(start, starts);
            end= max(end, ends);
        }
        return res;
    }
};
