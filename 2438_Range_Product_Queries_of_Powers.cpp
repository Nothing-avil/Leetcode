class Solution {
public:
    vector<int> productQueries(int n, vector<vector<int>>& queries) {
        vector<int> powers;
        const int mod = 1e9+7;
        while(n){
            int t = n & -n;
            powers.push_back(t);
            n -= t;
        }
        vector<int> answer;
        for(auto Q : queries){
            long long ans = 1LL;
            for(int i = Q[0]; i<= Q[1]; i++){
                ans = (ans * powers[i]) % mod;
            }
            answer.push_back(ans);
        }
        return answer;
    }
};
