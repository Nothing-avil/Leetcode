class Solution {
public:
    long long maximumTripletValue(vector<int>& nums) {
        int maxs = 0;
        long long diff = 0;
        long long ans = 0;
        for(int x : nums){
            ans = max(ans, diff*x);
            diff = max(diff, 1LL*maxs - x);
            maxs = max(maxs, x);
        }
        return ans;
    }
};
