class Solution {
public:
    vector<int> decrypt(vector<int>& code, int k) {
        int n = code.size();
        vector<int> res(n, 0);
        if(k==0){
            return res;
        }
        function<int(int)> solver = [&](int a){
            int sum = 0;
            if(k>0){
                for(int i=1; i<=k; i++){
                    sum += code[(a+i)%n];
                }
            }
            else{
                for(int i=1; i<=abs(k); i++){
                    sum += code[((n-1) + a + 1 - i)%n];
                }
            }
            return sum;
        };
        for(int i=0; i<n; i++){
            res[i] = solver(i);
        }
        return res;
    }
};
