class Solution {
public:
    int possibleStringCount(string word) {
        int cnt[26] = {0};
        for(int i=1; i<word.size(); i++){
            if(word[i] == word[i-1]){
                cnt[word[i] - 'a']++;
            }
        }
        int answer = 1;
        for(int i=0; i<26; i++){
            answer += cnt[i];
        }
        return answer;
    }
};
