class Solution {
public:
    int numSubseq(vector<int>& nums, int target) {
        int n = nums.size();
        const int mod = 1e9+7;
        int answer = 0;
        sort(nums.begin(), nums.end());
        vector<int> check(n, 1);
        for(int i=1; i<n; i++){
            check[i] = (check[i-1] * 2)%mod;
        }
        int i=0, j = n-1;
        while(i<=j){
            if(nums[i] + nums[j] > target){
                j--;
            }
            else{
                answer = (answer + check[j-i])%mod;
                i++;
            }
        }
        return answer;
    }
};
