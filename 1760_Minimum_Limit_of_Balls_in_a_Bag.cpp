class Solution {
public:
    int minimumSize(vector<int>& nums, int maxOperations) {
        int s =1, e = *max_element(nums.begin(), nums.end());
        while(s<e){
            int mid = (s+e)>>1;
            long long cnt = 0;
            for (int x : nums) {
                cnt += (x - 1) / mid;
            }
            if(cnt <= maxOperations){
                e = mid;
            }
            else{
                s = mid+1;
            }
        }
        return s;
    }
};
