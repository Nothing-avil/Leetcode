class Solution {
public:
    long long dividePlayers(vector<int>& skill) {
        sort(skill.begin(), skill.end());
        long long sums = accumulate(skill.begin(), skill.end(), 0ll);
        long long target = sums/(skill.size()/2);
        int i=0, j=skill.size()-1;
        long long total=0;
        while(i<j){
            if(target != (skill[i] + skill[j])){
                return -1;
            }
            total += skill[i]*skill[j];
            i++;
            j--;
        }
        return total;
    }
};
