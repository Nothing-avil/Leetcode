class Solution {
public:
    vector<int> findThePrefixCommonArray(vector<int>& A, vector<int>& B) {
        vector<int> cnt(A.size()+1), res(A.size());
        int c=0;
        for(int i=0; i<A.size(); i++){
            cnt[A[i]]++;
            if(cnt[A[i]] >= 2){
                c++;
            }
            cnt[B[i]]++;
            if(cnt[B[i]] >= 2){
                c++;
            }
            res[i] = c;
        }
        return res;
    }
};
