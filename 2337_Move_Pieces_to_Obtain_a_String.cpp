class Solution {
public:
    bool canChange(string start, string target) {
        int i=0, j=0;
        int n1= start.size(), n2= target.size();
        while(true){
            while (i < n1 && start[i] == '_') ++i;
            while (j < n2 && target[j] == '_') ++j;
            if(i==n1 && j==n2){
                return true;
            }
            if(i==n1 || j==n2 || start[i] != target[j]){
                return false;
            }
            if(start[i] == 'L' && i < j){
                return false;
            }
            if(start[i]== 'R' && i > j){
                return false;
            }
            i++;
            j++;
        }
    }
};
