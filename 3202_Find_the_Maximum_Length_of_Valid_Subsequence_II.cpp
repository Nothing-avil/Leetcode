class Solution {
public:
    int maximumLength(vector<int>& nums, int k) {
        int mod = k;
        int dp[mod][mod];
        int answer = 0;
        memset(dp, 0, sizeof(dp));
        for(int i : nums){
            i %= mod;
            for(int j = 0; j<mod; j++){
                int c = (j - i + mod)%mod;
                dp[i][c] = dp[c][i] + 1;
                answer = max(answer, dp[i][c]);
            }
        }
        return answer;
    }
};
