class Solution {
public:
    vector<int> lexicalOrder(int n) {
        vector<int> res;
        function<void(int)> dfs = [&](int s){
            if(s>n){
                return;
            }
            res.push_back(s);
            for(int i=0; i<=9; i++){
                dfs((s*10+i));
            }
        };
        for(int i=1; i<=9; i++){
            dfs(i);
        }
        return res;
    }
};
