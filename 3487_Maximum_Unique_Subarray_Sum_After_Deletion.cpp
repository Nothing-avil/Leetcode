class Solution {
public:
    int maxSum(vector<int>& nums) {
        unordered_map<int, int> mp;
        for(int i : nums){
            mp[i]++;
        }
        int answer = nums[0];
        int maxsum = nums[0];
        int c = nums[0] < 0 ? 1 : 0;
        mp.erase(nums[0]);
        for(int i = 1; i<nums.size(); i++){
            if(nums[i] < 0){
                c++;
                continue;
            }
            if(nums[i] >= 0 && mp[nums[i]]){
                maxsum = max(nums[i], maxsum + nums[i]);
                answer = max(answer, maxsum);
                mp.erase(nums[i]);
            }
        }
        return (c == nums.size()) ? *max_element(nums.begin(), nums.end()) : answer;
    }
};
