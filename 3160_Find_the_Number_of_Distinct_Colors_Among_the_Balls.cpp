class Solution {
public:
    vector<int> queryResults(int limit, vector<vector<int>>& queries) {
        unordered_map<int,int> l, co;
        int n = queries.size();
        vector<int> ans(n);
        for(int i=0; i<n; i++){
            co[queries[i][1]]++;
            if(l.contains(queries[i][0]) && --co[l[queries[i][0]]]==0){
                co.erase(l[queries[i][0]]);
            }
            l[queries[i][0]] = queries[i][1];
            ans[i] = co.size();
        }
        return ans;
    }
};
