class Solution {
public:
    int numberOfArrays(vector<int>& differences, int lower, int upper) {
        long long sum = 0;
        long long mx = 0, mn = 0;
        for(int i : differences){
            sum += i;
            mn = min(mn, sum);
            mx = max(mx, sum);
        }
        return max((upper - lower - (mx - mn) + 1), 0ll);
    }
};
