class Solution {
public:
    int closestTarget(vector<string>& words, string target, int startIndex) {
        int n = words.size();
        int answer = INT_MAX;
        for(int i=0; i<n; i++){
            if(target == words[i]){
                answer = min(answer, (startIndex - i + n) % n);
                answer = min(answer, (i - startIndex + n) % n);
            }
        }
        return answer == INT_MAX ? -1 : answer;
    }
};
