class Solution {
public:

    int N;
    int dp[1001][1001];
    int solver(int A, int clip){
        if(A==N){
            return 0;
        }
        if(A>N){
            return 100000;
        }
        if(dp[A][clip]!=-1){
            return dp[A][clip];
        }
        int c= solver(A+A, A)+2;
        int p= solver(A+clip, clip)+1;
        return dp[A][clip]= min(c,p);
    }


    int minSteps(int n) {
        if(n==1){
            return 0;
        }
        memset(dp, -1, sizeof(dp));
        N=n;
        return 1+solver(1, 1);
    }
};
