class Solution {
public:
    int countLargestGroup(int n) {
        int add[37] = {0};
        int ans = 0, mx = 0;
        for(int i = 1; i <= n; i++){
            int sum =0;
            for(int j = i; j; j /= 10){
                sum += (j % 10);
            }
            add[sum]++;
            if(mx < add[sum]){
                mx = add[sum];
                ans = 1;
            }
            else if(mx == add[sum]){
                ans++;
            }
        }
        return ans;
    }
};
