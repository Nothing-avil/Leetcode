class Solution {
public:
    int minimizedMaximum(int n, vector<int>& quantities) {
        int l=1;
        int r = 1e5;
        while(l<r){
            int mid = (l+r)>>1;
            int total=0;
            for(int i : quantities){
                total += (i+ mid - 1)/mid;
            }
            if(total <= n){
                r = mid;
            }
            else{
                l = mid+1;
            }
        }
        return l;
    }
}; 
