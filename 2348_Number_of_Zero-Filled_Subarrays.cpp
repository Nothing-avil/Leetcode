class Solution {
public:
    long long zeroFilledSubarray(vector<int>& nums) {
        long long answer = 0;
        long long count = 0;
        for(int i : nums){
            if(i ==0){
                count++;
            }
            else if(count != 0){
                answer += count * (count + 1ll)/2ll;
                count = 0;
            }
        }
        if(count != 0){
            answer += count * (count + 1ll)/2ll;
            count = 0;
        }
        return answer;
    }
};
