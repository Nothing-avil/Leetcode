class Solution {
public:
    int findLHS(vector<int>& nums) {
        unordered_map<int, int> mp;
        for(int i : nums){
            mp[i]++;
        }
        int answer = 0;
        for(auto it : mp){
            if(mp.count(it.first+1)){
                answer = max(answer, it.second + mp[it.first+1]);
            }
        }
        return answer;
    }
};
