class Solution {
public:
    string largestNumber(vector<int>& nums) {
        vector<string> pq;
        for(int i : nums){
            pq.push_back(to_string(i));
        }
        sort(pq.begin(), pq.end(), [](string a, string b){
            return a+b > b+a;
        });
        if(pq[0] == "0"){
            return "0";
        }
        string answer = "";
        int i=0;
        while(i < pq.size()){
            answer += pq[i++];
        }
        return answer;
    }
};
