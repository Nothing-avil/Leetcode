class Solution {
public:
    vector<bool> isArraySpecial(vector<int>& nums, vector<vector<int>>& queries) {
        vector<bool> res;
        int n=nums.size();
        vector<int> party(n);
        for(int i=1; i<n; i++){
            party[i]= party[i-1] + ((nums[i]%2)==(nums[i-1]%2)?1:0);
        }
        for(auto i: queries){
            int f= i[0];
            int t= i[1];
            int s= party[t]-party[f];
            res.push_back((s==0));
        }
        return res;
    }
};
