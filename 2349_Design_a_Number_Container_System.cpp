class NumberContainers {
public:
    unordered_map<int,int> mp;
    unordered_map<int, set<int>> pos;
    NumberContainers() {
        
    }
    
    void change(int index, int number) {
        if(mp.contains(index)){
            int val = mp[index];
            pos[val].erase(index);
            if(pos[val].empty()){
                pos.erase(val);
            }
        }
        mp[index] = number;
        pos[number].insert(index);
    }
    
    int find(int number) {
        return pos.contains(number) ? *pos[number].begin() : -1;
    }
};

