class Solution {
public:
    vector<int> smallestSubarrays(vector<int>& nums) {
        int n = nums.size();
        vector<int> answer(n);
        vector<int>bits(32, -1);
        for(int i= n-1; ~i; i--){
            int cal = 1;
            for(int j=0; j<32; j++){
                if((nums[i] >> j) & 1){
                    bits[j] = i;
                }
                else{
                    cal = max(cal, bits[j] - i + 1);
                }
            }
            answer[i] = cal;
        }
        return answer;
    }
};
