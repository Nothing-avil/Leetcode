class Solution {
public:
    int maxMoves(vector<vector<int>>& grid) {
        int n = grid.size();
        int m = grid[0].size();
        unordered_set<int> a, b;
        for (int i = 0; i < n; i++) {
            a.insert(i);
        }
        for (int j = 0; j < m - 1; ++j) {
            b.clear();
            for (int i : a) {
                for (int k = i - 1; k <= i + 1; ++k) {
                    if (k >= 0 && k < n && grid[i][j] < grid[k][j + 1]) {
                        b.insert(k);
                    }
                }
            }
            if (b.empty()) {
                return j;
            }
            a.swap(b);
        }
        return m - 1;
    }
};
