class Solution {
public:
    int maxTwoEvents(vector<vector<int>>& events) {
        int n= events.size();
        sort(events.begin(), events.end());
        vector<int> dp(n+1);
        for(int i=n-1; i; i--){
            dp[i] = max(dp[i+1], events[i][2]);
        }
        int ans = 0;
        for(auto& e : events){
            int a = e[2];
            int s=0, end = n;
            while(s < end){
                int mid= (s+end)>>1;
                if(events[mid][0] > e[1]){
                    end = mid;
                }
                else{
                    s = mid+1;
                }
            }
            if(s < n){
                a += dp[s];
            }
            ans = max(ans, a);
        }
        return ans;
    }
};
