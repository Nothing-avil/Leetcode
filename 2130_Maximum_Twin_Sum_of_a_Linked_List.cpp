/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    int pairSum(ListNode* head) {
        int mx= 0;
        ListNode* p = head, *pl=head;
        while (pl && pl!=NULL) {
            p= p->next;
            pl= pl->next->next;
        }
        ListNode* nad =NULL, *nxtnad;
        while(p!=NULL){
            nxtnad= p->next;
            p->next= nad;
            nad= p;
            p= nxtnad;
        }
        while(nad!=NULL){
            mx = max(nad->val+ head->val, mx);
            nad= nad->next;
            head= head->next;
        }
        return mx;
    }
};
