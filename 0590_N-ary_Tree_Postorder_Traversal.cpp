class Solution {
public:

    vector<int> ans ={};

    vector<int> postorder(Node* root) {
        if(root==NULL){
            return ans;
        }
        for(auto i : root->children){
            postorder(i);
        }
        ans.push_back(root->val);
        return ans;
    }
};
