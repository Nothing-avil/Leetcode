class Solution {
public:
    long long countGood(vector<int>& nums, int k) {
        int n= nums.size();
        int i=0, j =0, copy = 0;
        long long answer = 0;
        unordered_map<int, int> mp;
        while(j < n){
            copy += mp[nums[j]];
            mp[nums[j]]++;
            while(copy >= k){
                answer += (n - j);
                mp[nums[i]]--;
                copy -= mp[nums[i++]];
            }
            j++;
        }
        return answer;
    }
};
