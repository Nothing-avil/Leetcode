class Solution {
public:
    string getHappyString(int n, int k) {
        vector<string> res;
        string s = "";
        function<void()> solve = [&](){
            if(s.size() == n){
                res.push_back(s);
                return;
            }
            for(char c = 'a'; c <= 'c'; c++){
                if(!s.empty() && s.back() == c){
                    continue;
                }
                s.push_back(c);
                solve();
                s.pop_back();
            }
        };
        solve();
        if(res.size() < k){
            return "";
        }
        return res[k-1];
    }
};
