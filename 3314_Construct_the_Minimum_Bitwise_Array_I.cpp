class Solution {
public:
    vector<int> minBitwiseArray(vector<int>& nums) {
        vector<int> ans;
        for(int i : nums){
            bool check = true;
            for(int j=0; j<=i; j++){
                if(((j) | (j + 1)) == i){
                    ans.push_back(j);
                    check = false;
                    break;
                }
            }
            if(check){
                ans.push_back(-1);
            }
        }
        return ans;
    }
};
