class Solution {
public:
    vector<int> arrayRankTransform(vector<int>& arr) {
        vector<int> che = arr;
        sort(che.begin(), che.end());
        unordered_map<int, int> mp;
        for(int i=0, j=1; i<arr.size(); i++){
            if(!mp.count(che[i])){
                mp[che[i]]= j++;
            }
        }
        for(int i=0; i<arr.size(); i++){
            arr[i] = mp[arr[i]];
        }
        return arr;
    }
};
