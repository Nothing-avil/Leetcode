class Solution {
public:
    vector<vector<char>> rotateTheBox(vector<vector<char>>& box) {
        int m = box.size();
        int n = box[0].size();
        vector<vector<char>> res(n, vector<char>(m));
        for(int i=0; i<m; i++){
            for(int j=0; j<n; j++){
                res[j][m-i-1] = box[i][j];
            }
        }
        for(int i=0; i<m; i++){
            queue<int> qu;
            for(int j= n-1; ~j; j--){
                if(res[j][i] == '*'){
                    //remove;
                    queue<int> t;
                    swap(t, qu);
                }
                else if(res[j][i] == '.'){
                    //space
                    qu.push(j);
                }
                else if(!qu.empty()){
                    //stone put
                    res[qu.front()][i] = '#';
                    qu.pop();
                    res[j][i] = '.';
                    qu.push(j);
                }
            }
        }
        return res;
    }
};
