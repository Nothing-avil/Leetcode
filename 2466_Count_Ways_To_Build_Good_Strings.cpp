class Solution {
public:
    int countGoodStrings(int low, int high, int zero, int one) {
        int m=1e9+7;
        vector<int> dp(200005,0);
        dp[zero]+=1;
        dp[one]+=1;
        for(int i=0;i<=high;i++)
            dp[i]+=(dp[max(0,i-zero)]+dp[max(0,i-one)])%m;
        int ans=0;
        for(int i=low;i<=high;i++)
            ans=(dp[i]+ans)%m;
        return ans;
    }
};
