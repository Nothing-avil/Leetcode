class Solution {
public:
    bool divideArray(vector<int>& nums) {
        int mp[501] = {0};
        for(int i : nums){
            mp[i]++;
        }
        for(int i=0; i<501; i++){
            if(mp[i] & 1){
                return false;
            }
        }
        return true;
    }
};
