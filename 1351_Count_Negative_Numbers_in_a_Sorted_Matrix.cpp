class Solution {
public:
    int countNegatives(vector<vector<int>>& grid) {
        int n = grid.size();
        int m = grid[0].size();
        int i = 0, j = m -1;
        int answer = 0;
        while(i < n && j >=0){
            if(grid[i][j] >= 0){
                i++;
            }
            else{
                answer += (n - i);
                j--;
            }
        }
        return answer;
    }
};
