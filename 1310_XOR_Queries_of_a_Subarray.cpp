class Solution {
public:
    vector<int> xorQueries(vector<int>& arr, vector<vector<int>>& queries) {
        for(int i=1; i<arr.size(); i++){
            arr[i] = arr[i]^arr[i-1];
        }
        vector<int> answer(queries.size());
        for(int q = 0; q< queries.size(); q++){
            int l = queries[q][0];
            int r = queries[q][1];
            if(l>0){
                answer[q] = arr[r]^arr[l-1];
            }
            else{
                answer[q] = arr[r];
            }
        }
        return answer;
    }
};
