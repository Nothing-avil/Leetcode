class Solution {
public:
    vector<int> findRedundantConnection(vector<vector<int>>& edges) {
        int n = edges.size();
        vector<int> p(n);
        iota(p.begin(), p.end(), 0);
        function<int(int)> find = [&](int u){
            return u == p[u] ? u : p[u] = find(p[u]);
        };
        for(int i=0; i<n; i++){
            int x = find(edges[i][0] - 1);
            int q = find(edges[i][1] - 1);
            if(x == q){
                return edges[i];
            }
            p[x] = q;
        }
        return {};
    }
};
