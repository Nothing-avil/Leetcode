class Solution {
public:
    int findChampion(int n, vector<vector<int>>& edges) {
        int st[n];
        memset(st, 0, sizeof(st));
        for(auto i : edges){
            ++st[i[1]];
        }
        int c = 0;
        int s = 0;
        for(int i=0;i<n; i++){
            if(st[i] == 0){
                s=i;
                c++;
            }
        }
        return c==1 ? s : -1;
    }
};
