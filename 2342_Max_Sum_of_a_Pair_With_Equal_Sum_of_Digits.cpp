class Solution {
public:

    int findsum(int n){
        int s = 0;
        while(n>0){
            s += n%10;
            n /= 10;
        }
        return s;
    }

    int maximumSum(vector<int>& nums) {
        int res = -1;
        unordered_map<int,int> mp;
        for(int i=0; i<nums.size(); i++){
            int c = findsum(nums[i]);
            if(mp.count(c)){
                res = max(res, mp[c] + nums[i]);
            }
            mp[c] = max(mp[c], nums[i]);
        }
        return res;
    }
};
