class Solution {
public:
    int xorAfterQueries(vector<int>& nums, vector<vector<int>>& queries) {
        const int mod = 1e9+7;
        for(auto q : queries){
            for(int j = q[0]; j <= q[1]; j+=q[2]){
                nums[j] = (1ll * nums[j] * q[3]) % mod;
            }
        }
        int answer = 0;
        for(int i=0; i<nums.size(); i++){
            answer ^= nums[i];
        }
        return answer;
    }
};
