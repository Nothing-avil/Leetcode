class Solution {
public:
    int numTilePossibilities(string tiles) {
        vector<int> ch(26);
        for(char c : tiles){
            ch[c-'A']++;
        }
        int ans=0;
        function<void()> solve = [&](){
            ans++;
            for(int i=0; i<26; i++){
                if(ch[i] == 0){
                    continue;
                }
                ch[i]--;
                solve();
                ch[i]++;
            }
        };
        solve();
        return ans-1;
    }
};
