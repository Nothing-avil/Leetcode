class Solution {
public:
    vector<int> getMaximumXor(vector<int>& nums, int maximumBit) {
        int n = nums.size();
        int txor = 0;
        for(int i: nums){
            txor^=i;
        }
        vector<int> ans(n);
        int check = (1<<maximumBit) - 1;
        for(int i=0; i<n; i++){
            int k = txor^check;
            ans[i] = k;
            txor ^= nums[n-i-1];
        }
        return ans;
    }
};
