class Solution {
public:
    int xorAllNums(vector<int>& nums1, vector<int>& nums2) {
        int num1 = 0;
        if(nums1.size() & 1){
            for(int i : nums2){
                num1^=i;
            }
        }
        if(nums2.size() & 1){
            for(int i : nums1){
                num1^=i;
            }
        }
        return num1;
    }
};
