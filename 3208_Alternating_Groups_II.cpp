class Solution {
public:
    int numberOfAlternatingGroups(vector<int>& colors, int k) {
        int n = colors.size();
        int ans=0, c = 1;
        for(int i = 1; i < n << 1; i++){
            if (i && colors[i % n] == colors[(i - 1) % n]) {
                c = 1;
            } else {
                ++c;
            }
            ans += i >= n && c >= k ? 1 : 0;
        }
        cout << ans;
        return ans;
    }
};
