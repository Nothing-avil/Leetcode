class Solution {
public:
    int maxCount(vector<int>& banned, int n, int maxSum) {
        vector<int> dp(1e4+1, 1);
        for(int i : banned){
            dp[i] = 0;
        }
        int ans = 0;
        for(int i=1; i<=n; i++){
            if(dp[i] == 0){
                continue;
            }
            if(maxSum >= i){
                ans++;
                maxSum-=i;
                dp[i] = 0;
            }
        }
        return ans;
    }
};
