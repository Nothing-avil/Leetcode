class Solution {
public:
    int maxDistance(vector<int>& colors) {
        int answer = 0;
        int n = colors.size();
        for(int i=0; i<n; i++){
            if(colors[i] != colors[n - 1]){
                answer = max(answer, abs(i - (n - 1)));
            }
            if(colors[i] != colors[0]){
                answer = max(answer, i);
            }
        }
        return answer;
    }
};
