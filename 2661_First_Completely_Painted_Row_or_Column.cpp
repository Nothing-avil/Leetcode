class Solution {
public:
    int firstCompleteIndex(vector<int>& arr, vector<vector<int>>& mat) {
        int n = mat.size();
        int m = mat[0].size();
        vector<pair<int, int>> mp(n*m);
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                mp[mat[i][j] -1] = make_pair(i, j);
            }
        }
        vector<int> row(n), col(m);
        for(int i=0; i<arr.size(); i++){
            auto [a, b] = mp[arr[i]-1];
            row[a]++;
            col[b]++;
            if(col[b] == n || row[a] == m){
                return i;
            }
        }
        return arr.size();
    }
};
