class ProductOfNumbers {
public:
    vector<int> res;
    ProductOfNumbers() {
        res.push_back(1);
    }
    
    void add(int num) {
        if(num == 0){
            res.clear();
            res.push_back(1);
            return;
        }
        res.push_back(res.back()*num);
    }
    
    int getProduct(int k) {
        int n = res.size();
        if(n <= k){
            return 0;
        }
        return res.back()/res[n-k-1];
    }
};

