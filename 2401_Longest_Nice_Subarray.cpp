class Solution {
public:
    int longestNiceSubarray(vector<int>& nums) {
        int ans = 0, xors = 0;
        for(int l=0, r = 0; r < nums.size(); r++){
            while(xors & nums[r]){
                xors ^= nums[l++];
            }
            xors |= nums[r];
            ans = max(ans, (r-l+1));
        }
        return ans;
    }
};
