class Solution {
public:
    int countDays(int days, vector<vector<int>>& meetings) {
        sort(meetings.begin(), meetings.end());
        int result=0;
        vector<vector<int>> qu;
        for(auto v: meetings){
            if(qu.size()==0 || qu.back()[1] < v[0]){
                qu.push_back(v);
            }
            else{
                qu.back()[1]= max(qu.back()[1], v[1]);
            }
        }
        for(int i=0; i<qu.size(); i++){
            result += qu[i][1] - qu[i][0] +1;
        }
        return days- result;
    }
};
