class Solution {
public:
    int maximumCandies(vector<int>& candies, long long k) {
        int l=0;
        int h = *max_element(candies.begin(), candies.end());
        while(l<h){
            long long req = 0;
            int mid = (l + h + 1) >> 1;
            for(int c : candies){
                req += (c/mid);
            }
            if(req >= k){
                l = mid;
            }
            else{
                h = mid - 1;
            }
        }
        return l;
    }
};
