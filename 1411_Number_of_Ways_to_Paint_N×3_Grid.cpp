class Solution {
public:
    int numOfWays(int n) {
        int mod = 1e9 + 7;
        long long f0 = 6, f1 = 6;
        while (--n) {
            long long g0 = (f0 * 3 + f1 * 2) % mod;
            long long g1 = (f0 * 2 + f1 * 2) % mod;
            f0 = g0;
            f1 = g1;
        }
        return (int) (f0 + f1) % mod;
    }
};
