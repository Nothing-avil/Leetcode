class Solution {
public:
    int minSubarray(vector<int>& nums, int p) {
        int sum =0;
        for(int i=0; i<nums.size(); i++){
            sum = (sum+nums[i])%p;
        }
        int tar = sum;
        if(tar==0){
            return 0;
        }
        unordered_map<int, int> mp;
        int res =0, ans= INT_MAX;
        mp[0] = -1;
        for(int i=0; i<nums.size(); i++){
            res = (res + nums[i])%p;
            int re = (res - tar + p)%p;
            if(mp.count(re)){
                ans = min(ans, i - mp[re]);
            }
            mp[res] = i;
        }
        return ans == nums.size() ? -1 : ans;
    }
};
