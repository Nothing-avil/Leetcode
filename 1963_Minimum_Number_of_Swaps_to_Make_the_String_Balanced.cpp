class Solution {
public:
    int minSwaps(string s) {

        //APPROCH 1
        stack<char> st;
        for(char ch : s){
            if(st.empty()){
                st.push(ch);
                continue;
            }
            char cc = st.top();
            if(cc == '[' && ch== ']'){
                st.pop();
            }
            else{
                st.push(ch);
            }
        }
        return (ceil((double) st.size()/4.0));
        
        //APPROACH 2
        int x = 0;
        for (char& c : s) {
            if (c == '[') {
                ++x;
            } else if (x) {
                --x;
            }
        }
        return (x + 1) / 2;
    }
};
