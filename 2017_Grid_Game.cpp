class Solution {
public:
    long long gridGame(vector<vector<int>>& grid) {
        long long ans = LLONG_MAX;
        long long s1=0, s2=0;
        for(int i : grid[0]){
            s1 += i;
        }
        for(int i=0; i<grid[0].size(); i++){
            s1 -= grid[0][i];
            ans = min(ans, max(s1,s2));
            s2 += grid[1][i];
        }
        return ans;
    }
};
