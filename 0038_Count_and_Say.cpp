class Solution {
public:
    string countAndSay(int n) {
        string answer = "1";
        while(--n){
            string temp = "";
            for(int i = 0; i<answer.size();){
                int j = i;
                while(j < answer.size() && answer[i] == answer[j]){
                    j++;
                }
                temp += to_string(j-i);
                temp += answer[i];
                i = j;
            }
            answer = temp;
        }
        return answer;
    }
};
