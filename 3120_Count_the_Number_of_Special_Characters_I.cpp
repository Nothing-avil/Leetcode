class Solution {
public:
    int numberOfSpecialChars(string word) {
        vector<int> both(200, 0);
        int special =0;
        for(char c: word){
            both[c]++;
        }
        for(int i=0; i<200; i++){
            if(both[i]>0 && both[i+32]>0){
                special++;
            }
        }
        return special;
    }
};
