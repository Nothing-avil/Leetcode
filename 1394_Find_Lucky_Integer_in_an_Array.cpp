class Solution {
public:
    int findLucky(vector<int>& arr) {
        int cnt[501] = {0};
        int answer = -1;
        for(int i : arr){
            cnt[i]++;
        }
        for(int i=1; i<501; i++){
            if(cnt[i] == i){
                answer = max(answer, i);
            }
        }
        return answer;
    }
};
