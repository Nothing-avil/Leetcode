class Solution {
public:
    int nthUglyNumber(int n) {
        vector<int> dp(n+1);
        int a2=1,a3=1,a5=1;
        dp[1]=1;
        for(int i=2; i<=n; i++){
            int aa2= dp[a2]*2;
            int aa3= dp[a3]*3;
            int aa5= dp[a5]*5;
            dp[i] = min({aa2,aa3,aa5});
            if(dp[i]==aa2){
                a2++;
            }
            if(dp[i]==aa3){
                a3++;
            }
            if(dp[i]==aa5){
                a5++;
            }
        }
        return dp[n];
    }
};
