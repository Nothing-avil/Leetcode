class Solution {
public:
    bool canSortArray(vector<int>& nums) {
        bool check = true;
        do{
            check = false;
            for(int i=1; i<nums.size(); i++){
                if(__builtin_popcount(nums[i]) == __builtin_popcount(nums[i-1])){
                    if(nums[i-1] > nums[i]){
                        swap(nums[i], nums[i-1]);
                        check = true;
                    }
                }
            }
        }while(check);
        return is_sorted(nums.begin(), nums.end());
    }
};
