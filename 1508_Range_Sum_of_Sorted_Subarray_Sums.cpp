class Solution {
public:
    const int mod = 1e9+7;
    int rangeSum(vector<int>& nums, int n, int left, int right) {
        priority_queue<int, vector<int>, greater<int>> qu;
        int ans=0;
        for(int i=0; i<n; i++){
            int c=0;
            for(int j=i; j<n; j++){
                c+=nums[j];
                qu.push(c);
            }
        }
        int cc= right-left+1;
        left--;
        while(left--){
            int t= qu.top();
            cout<<t<<endl;
            qu.pop();
        }
        while(cc--){
            int t= qu.top();
            qu.pop();
            ans =(ans+t)%mod;
        }
        return ans;
    }
};
