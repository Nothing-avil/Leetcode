class Solution {
public:
    int minOperations(vector<int>& nums, int k) {
        priority_queue<long long, vector<long long>, greater<>> pq(nums.begin(), nums.end());
        int c = 0;
        while((pq.top() < k) && (pq.size()>=2)){
            long long a = pq.top();
            pq.pop();
            long long b = pq.top();
            pq.pop();
            long long s = min(a,b) * 2 + max(a,b);
            pq.push(s);
            c++;
        }
        return c;
    }
};
