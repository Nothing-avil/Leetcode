class Solution {
public:
    vector<string> getLongestSubsequence(vector<string>& words, vector<int>& groups) {
        vector<string> answer;
        for(int i=0; i<words.size(); i++){
            if(i == 0 || groups[i] != groups[i-1]){
                answer.push_back(words[i]);
            }
        }
        return answer;
    }
};
