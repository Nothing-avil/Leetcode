class Solution {
public:
    bool isZeroArray(vector<int>& nums, vector<vector<int>>& queries) {
        int n = nums.size();
        vector<int> dp(n + 1, 0);
        for(auto i : queries){
            dp[i[0]]++;
            dp[i[1] + 1]--;
        }
        int c = 0;
        for(int t=0; t<nums.size(); t++){
            c+=dp[t];
            if(nums[t] > c){
                return false;
            }
        }
        return true;
    }
};
