class Solution {
public:
    int mincostTickets(vector<int>& days, vector<int>& costs) {
        int dp[366] = {0};
        int n = days.size();
        int j = 0;
        for (int i = 1; i <= 365; i++) {
            if (j >= n) {
                break;
            }
            if (i != days[j]) {
                dp[i] = dp[i-1];
            } else {
                dp[i] = dp[i-1] + costs[0];
                dp[i] = min(dp[i], dp[max(i-7, 0)] + costs[1]);
                dp[i] = min(dp[i], dp[max(i-30, 0)] + costs[2]); 
                j++;
            }
        }
        return dp[days.back()];
    }
};
