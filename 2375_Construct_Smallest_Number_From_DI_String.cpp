class Solution {
public:
    string smallestNumber(string pattern) {
        string temp = "123456789";
        int n = pattern.size();
        for(int i=0; i<n; i++){
            for(int j=0; j<n; j++){
                if(pattern[j] == 'D' && temp[j] < temp[j+1]){
                    swap(temp[j], temp[j+1]);
                }
            }
        }
        return temp.substr(0, n+1);
    }
};
