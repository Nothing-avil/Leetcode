class Solution {
public:
    vector<int> partitionLabels(string s) {
        int n = s.size();
        vector<int> answer;
        unordered_map<int, int> mp;
        for(int i=0; i<n; i++){
            mp[s[i]] = i;
        }
        int idx=0, j=0;
        for(int i=0; i<n; i++){
            idx = max(idx, mp[s[i]]);
            if(idx == i){
                answer.push_back(i-j+1);
                j = i+1;
            }
        }
        return answer;
    }
};
