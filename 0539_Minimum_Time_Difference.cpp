class Solution {
public:
    int findMinDifference(vector<string>& timePoints) {
        vector<int> int_time;
        for( auto i : timePoints){
            int hr = stoi(i.substr(0, 2));
            int mn = stoi(i.substr(3));
            int_time.push_back((hr*60 + mn));
        }
        sort(int_time.begin(), int_time.end());
        int n = int_time.size();
        int mid_ni = 1440;
        int diff = abs(int_time[n-1]-mid_ni) + int_time[0];
        for(int i=0; i<n-1; i++){
            diff = min(diff, abs(int_time[i] - int_time[i+1]));
        }
        return diff;
    }
};
