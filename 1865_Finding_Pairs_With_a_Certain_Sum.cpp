class FindSumPairs {
public:
    vector<int> nums1, nums2;
    unordered_map<int, int> mp;
    FindSumPairs(vector<int>& nums1, vector<int>& nums2) {
        this->nums1= nums1;
        this->nums2 = nums2;
        for(int i : nums2){
            mp[i]++;
        }
    }
    
    void add(int index, int val) {
        int v = nums2[index];
        mp[v]--;
        mp[v + val]++;
        nums2[index] += val;
    }
    
    int count(int tot) {
        int res = 0;
        for(int i = 0; i < nums1.size(); i++){
            if(mp.contains(tot - nums1[i])){
                res += mp[(tot - nums1[i])];
            }
        }
        return res;
    }
};

