class Solution {
public:
    long long putMarbles(vector<int>& weights, int k) {
        int n = weights.size();
        long long answer = 0;
        vector<int> temp(n-1);
        for(int i=0; i<n-1; i++){
            temp[i] = weights[i] + weights[i+1];
        }
        sort(temp.begin(), temp.end());
        for(int i=0; i<k-1; i++){
            answer -= temp[i];
            answer += temp[n-2-i];
        }
        return answer;
    }
};
