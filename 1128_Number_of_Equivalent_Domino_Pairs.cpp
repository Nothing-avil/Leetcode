class Solution {
public:
    int numEquivDominoPairs(vector<vector<int>>& dominoes) {
        int temp[100] = {0};
        int answer = 0;
        for(auto i : dominoes){
            int val = 0;
            if(i[0] < i[1]){
                val = i[0] * 10 + i[1];
            }
            else{
                val = i[1] * 10 + i[0];
            }
            answer += temp[val];
            temp[val]++;
        }
        return answer;
    }
};
