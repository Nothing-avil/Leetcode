class Solution {
public:
    long long minSum(vector<int>& nums1, vector<int>& nums2) {
        long long answer1 = 0, answer2 = 0;
        bool check = false;
        for(int i : nums1){
            check |= i == 0;
            answer1 += max(i, 1);
        }
        for(int i : nums2){
            answer2 += max(i, 1);
        }
        if(answer1 > answer2){
            return minSum(nums2, nums1);
        }
        if(answer1 == answer2){
            return answer1;
        }
        return check ? answer2 : -1;
    }
};
