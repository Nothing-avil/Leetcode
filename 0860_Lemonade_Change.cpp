class Solution {
public:
    bool lemonadeChange(vector<int>& bills) {
        int f5=0, t10=0, t20=0;
        for(int i: bills){
            if(i!=5){
                if(i==10){
                    if(f5<1){
                        return false;
                    }
                    f5--;
                    t10++;
                }else{
                    if(f5<3 && t10<1){
                        return false;
                    }
                    if(t10<1){
                        if(f5<3){
                            return false;
                        }
                        f5-=3;
                    }else{
                        if(f5<1){
                            return false;
                        }
                        t10--;
                        f5--;
                    }
                }
            }else{
                f5++;
            }
        }
        return true;
    }
};
