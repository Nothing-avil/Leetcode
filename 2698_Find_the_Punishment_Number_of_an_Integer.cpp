class Solution {
public:

    bool test(string s, int i, int val){
        if(i >= s.size()){
            return val == 0;
        }
        int sum = 0;
        for(int j = i; j<s.size(); j++){
            sum = sum*10 + s[j] - '0';
            if(sum > val){
                break;
            }
            if(test(s, j+1, val - sum)){
                return true;
            }
        }
        return false;
    }

    int punishmentNumber(int n) {
        int ans = 0;
        for(int i=1; i<=n; i++){
            int powe = i*i;
            string str = to_string(powe);
            if(test(str, 0, i)){
                ans += powe;
            }
        }
        return ans;
    }
};
