class Solution {
public:
    int minOperations(vector<int>& arr) {
        int c = 0;
        for(int i = 0; i < arr.size(); i++){
            if(arr[i] == 0){
                if(i+2 >= arr.size()){
                    return -1;
                }
                arr[i+1]^=1;
                arr[i+2]^=1;
                c++;
            }
        }
        return c;
    }
};
