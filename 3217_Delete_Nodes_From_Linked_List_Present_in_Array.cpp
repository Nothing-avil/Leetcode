class Solution {
public:
    ListNode* modifiedList(vector<int>& nums, ListNode* head) {
        ListNode* dummy = new ListNode(0, head);
        ListNode* pre= dummy;
        set<int> st(nums.begin(), nums.end());
        while(pre->next){
            if(st.find(pre->next->val) != st.end()){
                pre->next= pre->next->next;
            }
            else{
                pre= pre->next;
            }
        }
        return dummy->next;
    }
};
