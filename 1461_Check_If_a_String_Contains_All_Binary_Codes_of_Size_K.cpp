class Solution {
public:
    bool hasAllCodes(string s, int k) {
        int n = s.size();
        int checker = 1 << k;
        if(n - k + 1 < checker){
            return false;
        }
        unordered_set<string> st;
        for(int i=0; i+k <= n; i++){
            st.insert(move(s.substr(i, k)));
        }
        return st.size() == checker;
    }
};
