class Solution {
public:
    long long maxMatrixSum(vector<vector<int>>& matrix) {
        long long sum = 0;
        int mini = INT_MAX, neg = 0;
        for(auto i : matrix){
            for(int j : i){
                sum += abs(j);
                mini = min(mini, abs(j));
                neg += (j < 0);
            }
        }
        if(neg%2==0 || mini == 0){
            return sum;
        }
        return sum - (mini*2);
    }
};
