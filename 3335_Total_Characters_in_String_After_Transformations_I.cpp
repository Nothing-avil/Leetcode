class Solution {
public:
    int lengthAfterTransformations(string s, int t) {
        const int mod = 1e9 + 7;
        int freq[26] = {0};
        for(char ch : s){
            freq[ch - 'a']++;
        }
        for(int i = 0; i<t; i++){
            int test = freq[25];
            for(int j=25; j>0; j--){
                freq[j] = freq[j-1];
            }
            freq[0] = 0;
            freq[1] = (freq[1] + test) % mod;
            freq[0] = (freq[0] + test) % mod;
        }
        int answer = 0;
        for(int i=0; i<26; i++){
            answer = (answer + freq[i]) % mod;
        }
        return answer;
    }
};
