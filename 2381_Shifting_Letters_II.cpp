class Solution {
public:
    string shiftingLetters(string s, vector<vector<int>>& shifts) {
        vector<int> pre(s.size()+1, 0);
        for(auto it : shifts){
            int i = it[2];
            pre[it[0]] += (2*i - 1);
            pre[it[1]+1] -= (2*i -1);
        }
        for(int i=0;i<s.size(); i++){
            pre[i+1] = (pre[i+1] + pre[i])%26;
        }
        pre[0] = pre[0]%26;
        for(int i=0;i<s.size(); i++){
            int x=  (26 + (s[i] -'a') + pre[i])%26;
            s[i] = 'a' + x;
        }
        return s;
    }
};
