class Solution {
public:
    int longestPalindrome(vector<string>& words) {
        unordered_map<string, int> mp;
        for(string ch : words){
            mp[ch]++;
        }
        int answer = 0;
        int x =0;
        for(auto[a, v] : mp){
            string ra = a;
            reverse(ra.begin(), ra.end());
            if(a[1] == a[0]){
                x += v & 1;
                answer += (v/2)*2*2;
            }
            else if(mp.count(ra)){
                answer += min(v, mp[ra]) * 2;
            }
        }
        answer += x ? 2 : 0;
        return answer;
    }
};
