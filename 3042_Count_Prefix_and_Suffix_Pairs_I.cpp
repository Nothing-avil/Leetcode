class Solution {
public:
    bool solve(string str1, string str2){
        int x= str1.size();
        int y= str2.size();
        if(str1== str2.substr(0,x) && str1== str2.substr((y-x), y)){
            return true;
        }
        return false;
    }
    int countPrefixSuffixPairs(vector<string>& words) {
        int c=0;
        int n= words.size();
        for(int i=0; i<n; i++){
            int x= words[i].size();
            for(int j=i+1; j<n; j++){
                c+= solve(words[i], words[j]);
            }
        }
        return c;
    }
};
