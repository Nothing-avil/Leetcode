class MyCalendar {
public:
    vector<pair<int,int>> rec;
    MyCalendar() {

    }
    
    bool book(int start, int end) {
        for(auto i : rec){
            if(i.first < end && i.second > start){
                return false;
            }
        }
        rec.push_back({start, end});
        return true;
    }
};

