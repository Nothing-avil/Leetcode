class Solution {
public:
    bool areAlmostEqual(string s1, string s2) {
        int c[26] = {0};
        for(char s : s1){
            c[s-'a']++;
        }
        for(char s : s2){
            c[s-'a']--;
        }
        for(int i=0; i<26; i++){
            if(c[i]){
                return false;
            }
        }
        int ss = 0;
        for(int i=0; i<s1.size(); i++){
            if(s1[i] != s2[i]){
                ss++;
            }
            if(ss > 2){
                return false;
            }
        }
        return true;
    }
};
