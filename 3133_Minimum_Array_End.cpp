class Solution {
public:
    long long minEnd(int n, int x) {
        vector<long long> res;
        for(long long i=0; i<32; i++){
            if(!((1<<i)&x)){
                res.push_back(i);
            }
        }
        for(long long i=32; i<64; i++){
            res.push_back(i);
        }
        long long c=x;
        long long cnt = n-1;
        for(int i=0; i<res.size(); i++, cnt>>=1){
            c += (cnt & 1) << res[i];
        }
        return c;
    }
};
