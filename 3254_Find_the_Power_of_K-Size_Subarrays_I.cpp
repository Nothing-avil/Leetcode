class Solution {
public:
    vector<int> resultsArray(vector<int>& nums, int k) {
        vector<int> res(nums.size()-k+1);
        function<bool(int, int)> check = [&](int i, int  j){
            while(i<j){
                if(nums[i]+1 != nums[i+1]){
                    return false;
                }
                i++;
            }
            return true;
        };
        int i=0, j=k-1;
        while(j<nums.size()){
            if(check(i, j)){
                res[i] = nums[j];
            }
            else{
                res[i] = -1;
            }
            i++;
            j++;
        }
        return res;
    }
};
