class Solution {
public:
    int minAddToMakeValid(string s) {
      //APPROCH 1
      stack<char> st;
      for(char ch : s){
          if(st.empty()){
              st.push(ch);
              continue;
          }
          char cc = st.top();
          if(ch == ')' && cc == '('){
              st.pop();
          }
          else{
              st.push(ch);
          }
      }
      return st.size();
      
      //APPROCH 2
        int c=0, d=0;
        for(char ch : s){
            if(ch == '('){
                c++;
            }
            else{
                c>0 ? c-- : d++;
            }
        }
        return c+d;
    }
};
