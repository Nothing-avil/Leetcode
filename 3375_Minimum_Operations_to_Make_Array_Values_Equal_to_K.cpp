class Solution {
public:
    int minOperations(vector<int>& nums, int k) {
        unordered_map<int, int> freq;
        int val = INT_MAX;
        for(int i : nums){
            freq[i]++;
            val = min(val, i);
        }
        if(val < k){
            return -1;
        }
        int answer = freq.size();
        if(freq[k]){
            answer--;
        }
        return answer;
    }
};
