class Solution {
public:
    int areaOfMaxDiagonal(vector<vector<int>>& dimensions) {
        int ares=0;
        double dis=0;
        int n= dimensions.size();
        for(int i=0;i<n; i++){
            double ar= sqrt(dimensions[i][0]*dimensions[i][0]+ dimensions[i][1]*dimensions[i][1]);
            int areas= dimensions[i][0]*dimensions[i][1];
            if(ar>dis|| ar==dis && areas>ares){
                dis=ar;
                ares= areas;
            }
        }
        return ares;
    }
};
