class Solution {
public:
    vector<int> constructTransformedArray(vector<int>& nums) {
        int n = nums.size();
        vector<int> res(n);
        for(int i=0; i<n; i++){
            if(nums[i]==0){
                res[i] = nums[i];
            }
            else if(nums[i] > 0){
                int j = nums[i]%n;
                res[i] = nums[(i+j)%n];
            }
            else {
                int j = abs(nums[i]);
                res[i]= nums[(i +(n-(j%n)))%n];
            }
        }
        return res;
    }
};
