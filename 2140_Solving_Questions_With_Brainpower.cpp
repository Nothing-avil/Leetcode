class Solution {
public:
    long long mostPoints(vector<vector<int>>& questions) {
        long long n = questions.size();
        long long store[100001];
        memset(store, -1, sizeof(store));
        function<long long(int)> solver = [&](long long idx) -> long long{
            if(idx >= n){
                return 0;
            }
            if(store[idx] != -1){
                return store[idx];
            }
            long long take = questions[idx][0] + solver(idx + questions[idx][1] + 1);
            long long nottake = solver(idx+1);
            return store[idx] = max(take, nottake);
        };
        return solver(0);
    }
};
