class Solution {
public:
    string makeFancyString(string s) {
        int j=2;
        int n=s.size();
        if(n<=2){
            return s;
        }
        for(int k=2; k<n; k++){
            if(s[j-1]!=s[k] || s[j-2]!= s[k]){
                s[j++] = s[k];
            }
        }
        s.resize(j);
        return s;
    }
};
