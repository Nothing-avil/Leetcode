class Solution {
public:
    int countConsistentStrings(string allowed, vector<string>& words) {
        unordered_map<char,int>mp;
        for(char ch:allowed){
            mp[ch]++;
        }
        int ans=0;
        for(string s:words){
            int aa=0;
            for(char c :s){
                if(mp[c]>0){
                    aa++;
                }
            }
            if(aa==s.size()){
                ans++;
            }
        }
        return ans;
    }
};
