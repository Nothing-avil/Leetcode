class Solution {
public:
    void setZeroes(vector<vector<int>>& matrix) {
        int dp[201] = {0};
        int dp1[201] = {0};
        int n = matrix.size();
        int m = matrix[0].size();
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                if(matrix[i][j] == 0){
                    dp[i]++;
                    dp1[j]++;
                }
            }
        }
        for(int i=0; i<n; i++){
            for(int  j=0; j<m; j++){
                if(dp[i] != 0 || dp1[j] != 0){
                    matrix[i][j] = 0;
                }
            }
        }
    }
};
