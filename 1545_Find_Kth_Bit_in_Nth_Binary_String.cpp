class Solution {
public:
    char findKthBit(int n, int k) {
        if(k==1){
            return '0';
        }
        string str= "0";
        while(--n){
            string s= str;
            int mid = s.size()/2;
            if (s[mid] == '0') {
                s[mid] = '1';
            } else {
                s[mid] = '0';
            }
            str = str + '1' + s;
        }
        return str[k-1];
    }
};
